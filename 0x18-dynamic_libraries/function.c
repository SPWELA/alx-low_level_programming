#include <stdio.h>

/**
 * add - add two numbers from input
 * @a: first parameter
 * @b: second parameter
 *
 * Descripyion: adds two numbers
 * Return: always (0)
 */

int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtract two numbers from input
 * @a: first parameter
 * @b: second parameter
 *
 * Description: subtract two numbers
 * Return: always (0)
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers from input
 * @a: first parameter
 * @b: second parameter
 *
 * Description: multiply two numbers
 * Return: always (0)
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers from input
 * @a: first parameter
 * @b: second parameter
 *
 * Description: divides two numbers
 * Return: always (0)
 */

int div(int a, int b)
{
	return (a / b);
}
